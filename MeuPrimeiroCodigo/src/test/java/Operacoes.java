
public class Operacoes {
	
	
	public void soma (int n1, int n2) {
		
		int soma = n1 + n2;
		System.out.println("O resultado da soma é de: " + soma);
	}

	
	public void dividir (int n1, int n2) {
		int dividir = n1 / n2;
		System.out.println(" O resultado da divisão é de: " + dividir);
	}
	
	public void subtrair (int n1, int n2) {
		int subtrair = n1 - n2;
		System.out.println(" O resutado da subtração é de: " + subtrair);
		
	}
	
	public void multiplicacao (int n1, int n2) {
		int multiplicacao = n1 * n2;
		System.out.println(" O resultado da multiplicação é de: " + multiplicacao);
	}
	}

	
	

